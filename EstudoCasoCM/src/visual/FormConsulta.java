    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int opcao = JOptionPane.showOptionDialog(null, "Confirmar exclusão?",
                "Pergunta", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,
                null, new String[]{"Sim", "Não"}, "Sim");
        if (opcao == 0) {
            int linhaSelecionada = tblConsulta.getSelectedRow();
            Consultas objConsultas = listConsulta.get(linhaSelecionada);
            objDAOConsultas.remover(objConsultas);
            atualizaTabela();
            trataEdicao(false);
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnteriorActionPerformed
        // TODO add your handling code here:
        int linha = tblConsulta.getSelectedRow();
        if ((linha - 1) >= 0) {
            linha--;
        }
        tblConsulta.setRowSelectionInterval(linha, linha);
        tblConsulta.scrollRectToVisible(tblConsulta.getCellRect(linha, 0, true));

    }//GEN-LAST:event_btnAnteriorActionPerformed

    private void btnProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProximoActionPerformed
        // TODO add your handling code here:
        int linha = tblConsulta.getSelectedRow();
        if ((linha + 1) <= (tblConsulta.getRowCount()) - 1) {
            linha++;
        }
        tblConsulta.setRowSelectionInterval(linha, linha);
        tblConsulta.scrollRectToVisible(tblConsulta.getCellRect(linha, 0, true));
    }//GEN-LAST:event_btnProximoActionPerformed

