    public void atualizaTabela() {
        listConsulta.clear();
        listConsulta.addAll(objDAOConsultas.getLista());
        int linha = listConsulta.size() - 1;
        if (linha >= 0) {
            tblConsulta.setRowSelectionInterval(linha, linha);
            tblConsulta.scrollRectToVisible(tblConsulta.getCellRect(linha, linha, true));
        }
    }

    private void trataEdicao(boolean editando) {
        btnCancelar.setEnabled(editando);
        btnSalvar.setEnabled(editando);
        btnEditar.setEnabled(!editando);
        int linha = listConsulta.size() - 1;
        if (linha < 0) {
            btnEditar.setEnabled(false);
            btnExcluir.setEnabled(false);
            txtCodigo.setText("");
            txtData.setText("");
            txtValor.setText("");   
        } else {
            btnExcluir.setEnabled(!editando);
        }
        btnNovo.setEnabled(!editando);
        btnFechar.setEnabled(!editando);
        btnPrimeiro.setEnabled(!editando);
        btnProximo.setEnabled(!editando);
        btnAnterior.setEnabled(!editando);
        btnUltimo.setEnabled(!editando);
        txtData.setEnabled(editando);
        cbxMedico.setEnabled(editando);
        cbxPaciente.setEnabled(editando);
        txtValor.setEnabled(editando);
        tblConsulta.setEnabled(editando);

    }

    public boolean validaCampos() {
        
        if (txtData.getText().length() > 0) {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            sdf.setLenient(false);
            try {
                sdf.parse(txtData.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Informe a data da consulta");
                txtData.requestFocus();
                return false;
            }

        }
        
         if (!(cbxMedico.getSelectedIndex() >= 0)) {
            JOptionPane.showMessageDialog(null, "Selecione um medico");
            cbxMedico.requestFocus();
            return false;
        }
         
          if (!(cbxPaciente.getSelectedIndex() >= 0)) {
            JOptionPane.showMessageDialog(null, "Selecione um paciente");
            cbxPaciente.requestFocus();
            return false;
        }
        
        if (!(txtValor.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe o valor da Consulta");
            txtValor.requestFocus();
            return false;
        }
        
        return true;
    }
    

